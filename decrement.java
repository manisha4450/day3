import java.util.*;
public class decrement{
    public static void main(String[] args){
        // post increment
        int n=5;
        System.out.println(n++);
        //pre increment
        int a=4;
        System.out.println(++a);
        //pre decrement
        int b=6;
        System.out.println(--b);
        //post decrement
        int a1=56;
        System.out.println(a1--);
    }
}